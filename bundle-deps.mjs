/**
 * bundle-deps.mjs
 *
 * Concatenates @keepass-web library dist files in dependency order, stripping
 * ES module import/export syntax so they can be inlined into a single <script>
 * tag by the inliner. No external dependencies — runs with Node 22+.
 *
 * Usage: node bundle-deps.mjs <output-path>
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const NM = join(__dirname, 'node_modules');

// Files in dependency order. index.js re-export files are omitted —
// all declarations are in scope from the individual source files.
const FILES = [
  // @keepass-web/chacha20 (no external deps)
  '@keepass-web/chacha20/dist/src/index.js',

  // @keepass-web/argon2 (no external deps)
  '@keepass-web/argon2/dist/src/blake2b.js',
  '@keepass-web/argon2/dist/src/argon2.js',
  '@keepass-web/argon2/dist/src/index.js',

  // @keepass-web/kdbx (depends on chacha20 and argon2 above)
  '@keepass-web/kdbx/dist/src/bytes.js',
  '@keepass-web/kdbx/dist/src/xml.js',
  '@keepass-web/kdbx/dist/src/constants.js',
  '@keepass-web/kdbx/dist/src/variant-dictionary.js',
  '@keepass-web/kdbx/dist/src/crypto.js',
  '@keepass-web/kdbx/dist/src/key.js',
  '@keepass-web/kdbx/dist/src/credentials.js',
  '@keepass-web/kdbx/dist/src/protected-stream.js',
  '@keepass-web/kdbx/dist/src/hashed-block-stream.js',
  '@keepass-web/kdbx/dist/src/hmac-block-stream.js',
  '@keepass-web/kdbx/dist/src/header.js',
  '@keepass-web/kdbx/dist/src/inner-header.js',
  '@keepass-web/kdbx/dist/src/kdf.js',
  '@keepass-web/kdbx/dist/src/model.js',
  '@keepass-web/kdbx/dist/src/kdbx.js',
];

/**
 * Strip ES module syntax from a compiled JS file so it can be concatenated
 * with other files in a single classic <script> scope.
 *
 * Rules:
 *   import { ... } from '...'   → removed (names are in scope from prior files)
 *   export { ... } from '...'   → removed (re-exports, not needed)
 *   export { ... }              → removed (names already declared below)
 *   export function/class/const → strip the `export` keyword only
 */
function stripModuleSyntax(src) {
  return (
    src
      // Remove import statements (single-line; compiled tsc output is always single-line)
      .replace(/^import\b.*$/gm, '')
      // Remove re-export lines:  export { ... } from '...'
      .replace(/^export\s*\{[^}]*\}\s*from\s*['"][^'"]*['"];?\s*$/gm, '')
      // Remove standalone export lists:  export { X, Y };
      .replace(/^export\s*\{[^}]*\};?\s*$/gm, '')
      // Strip export keyword from declarations, preserving the declaration itself
      .replace(/^export\s+(async\s+function|function|class|const|let|var)\b/gm, '$1')
  );
}


const [, , outPath] = process.argv;
if (!outPath) {
  process.stderr.write('Usage: node bundle-deps.mjs <output-path>\n');
  process.exit(1);
}

// Symbols that page.js references as bare globals. Everything else stays
// scoped inside the IIFE so duplicate const names across library files don't
// cause SyntaxErrors.
const EXPORTS = [
  'Kdbx',
  'Credentials',
  'ProtectedValue',
  'getChildren',
  'getChild',
  'getText',
  'getAttribute',
  'setAttribute',
  'createElement',
  'appendChild',
  'createEntry',
  'createGroup',
];

const parts = [
  '/* keepass-web library bundle — generated by bundle-deps.mjs */',
  '(function() {',
];

for (const file of FILES) {
  let src = readFileSync(join(NM, file), 'utf8');
  src = stripModuleSyntax(src);
  parts.push(`\n/* === ${file} === */`);
  parts.push(src);
}

// Hoist the public API to globalThis so page.js can reach it.
parts.push('\n/* --- public exports --- */');
for (const name of EXPORTS) {
  parts.push(`if (typeof ${name} !== 'undefined') globalThis.${name} = ${name};`);
}
parts.push('})();');

writeFileSync(outPath, parts.join('\n'), 'utf8');
process.stdout.write(`Bundled ${FILES.length} files to ${outPath}\n`);
